#include "libft_test.h"
#include "libunit.h"

int		c_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	while ((s1[i] || s2[i]) && i < n)
	{
		if (s1[i] != s2[i])
			return ((unsigned char)s1[i] - (unsigned char)s2[i]);
		i++;
	}
	return (0);
}

int		strncmp_launcher(void)
{
	t_unit_test *testlist;

	testlist = NULL;
	my_printf("STRNCMP:\n");
	load_test(&testlist, "Basic test", &strncmp_basic_test);
	load_test(&testlist, "NULL test", &strncmp_null_test);
	load_test(&testlist, "Short test", &strncmp_short_test);
	return (launch_tests(&testlist));
}
